package com.example.flight;

public class Flight {
 int flightNumber;
  String Airline;
  String fromPlace;
  String toPlace;
  int businesClassSeat;
  int nonBusinessClassSeat;
  float ticketPrice;
  int numberOfRow;
  String meal;
public int getFlightNumber() {
	return flightNumber;
}
public void setFlightNumber(int flightNumber) {
	this.flightNumber = flightNumber;
}
public String getAirline() {
	return Airline;
}
public void setAirline(String airline) {
	Airline = airline;
}
public String getFromPlace() {
	return fromPlace;
}
public void setFromPlace(String fromPlace) {
	this.fromPlace = fromPlace;
}
public String getToPlace() {
	return toPlace;
}
public void setToPlace(String toPlace) {
	this.toPlace = toPlace;
}
public int getBusinesClassSeat() {
	return businesClassSeat;
}
public void setBusinesClassSeat(int businesClassSeat) {
	this.businesClassSeat = businesClassSeat;
}
public int getNonBusinessClassSeat() {
	return nonBusinessClassSeat;
}
public void setNonBusinessClassSeat(int nonBusinessClassSeat) {
	this.nonBusinessClassSeat = nonBusinessClassSeat;
}
public float getTicketPrice() {
	return ticketPrice;
}
public void setTicketPrice(float ticketPrice) {
	this.ticketPrice = ticketPrice;
}
public int getNumberOfRow() {
	return numberOfRow;
}
public void setNumberOfRow(int numberOfRow) {
	this.numberOfRow = numberOfRow;
}
public String getMeal() {
	return meal;
}
public void setMeal(String meal) {
	this.meal = meal;
}
  
  
}
